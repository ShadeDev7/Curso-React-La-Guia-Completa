// Tipos de datos

// Undefined (no se ha definido el valor de la variable)
let variableUndefined;

console.log(typeof variableUndefined);

// Boolean (verdadero o falso)
const variableBooleana = true;

console.log(typeof variableBooleana);

// Number (números enteros y decimales)
let variableEntera = 7;
let variableDecimal = 7.27;

console.log(typeof variableEntera);
console.log(typeof variableDecimal);

// String (cadena de carácteres)
let variableCadena = "Hola mundo";

console.log(typeof variableCadena);

// BigInt (números enteros grandes)
let variableEnteraGrande = BigInt(1234567890123456789012345678901234567890);

console.log(typeof variableEnteraGrande);

// Symbol (simbolos únicos)
let variableSimbolo = Symbol("A");

console.log(typeof variableSimbolo);

// Null (valor nulo o vacío)
let variableNula = null;

console.log(typeof variableNula);
